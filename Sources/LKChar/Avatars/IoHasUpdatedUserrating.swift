
import UIKit
import TTLBGenerals

class IoHasUpdatedUserrating: HeMonoDwarfWriter {
          
        
        
    var oNeedTruncate:[Any]?
    var rRoundPages = 0
        
        
    func withModifyTime(_ array : NSArray) {
        var nLinkTask = [Any]()
        for lFabEnvira in array as! [Any]{
            let oBundledPath = IoTokenService(dict: lFabEnvira)
            oBundledPath.setResourceValue((lFabEnvira as! NSDictionary).setLoadingBackgroundColor(dMallocString))
            nLinkTask.append(oBundledPath)
        }
        self.oNeedTruncate = nLinkTask
        rRoundPages = oNeedTruncate!.count
    }
 }
